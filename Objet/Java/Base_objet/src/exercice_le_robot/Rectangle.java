package exercice_le_robot;

public class Rectangle {
	
	//attributs
	private int longueur;
	private int largeur;
	
	//constructeur
	public Rectangle(int _longueur, int _largeur) {
		longueur = _longueur;
		largeur = _largeur;
	}
	
	//getters
	public int getLongueur() {
		return longueur;
	}
	
	public int getLargeur() {
		return largeur;
	}
}
